#
num_list = [12, 18, 4, 119, 17]
# uppend
num_list.append(99)
print(num_list)

# extent
num_list.extend(['111'])
print(num_list)

# pop
num_list.pop(-1)
print(num_list)

# remove
num_list.remove(4)
print(num_list)

# index
print(num_list.index(119))

# isert
num_list.insert(0, 4)
print(num_list)

# sort
num_list.sort()
print(num_list)

num_list.sort(reverse=True)
print(num_list)

# sorted
print(sorted(num_list))

#copy
print(num_list.copy())

#min max summ
print(min.num_list())
print(max.num_list())
print(sum.num_list())







